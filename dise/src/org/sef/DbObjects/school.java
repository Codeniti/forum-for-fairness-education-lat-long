package org.sef.DbObjects;

public class school {
	private String address;
	private int postalcode;
	private double lat;
	private double lng;
	private double nelat;
	private double nelng;
	private double selat;
	private double selng;
	private String schoolname;
	private int schoolid;
	private Boolean superceded;

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address
	 *            the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the postalcode
	 */
	public int getPostalcode() {
		return postalcode;
	}

	/**
	 * @param postalcode
	 *            the postalcode to set
	 */
	public void setPostalcode(int postalcode) {
		this.postalcode = postalcode;
	}

	/**
	 * @return the lat
	 */
	public double getLat() {
		return lat;
	}

	/**
	 * @param lat
	 *            the lat to set
	 */
	public void setLat(double lat) {
		this.lat = lat;
	}

	/**
	 * @return the lng
	 */
	public double getLng() {
		return lng;
	}

	/**
	 * @param lng
	 *            the lng to set
	 */
	public void setLng(double lng) {
		this.lng = lng;
	}

	/**
	 * @return the nelat
	 */
	public double getNelat() {
		return nelat;
	}

	/**
	 * @param nelat
	 *            the nelat to set
	 */
	public void setNelat(double nelat) {
		this.nelat = nelat;
	}

	/**
	 * @return the nelng
	 */
	public double getNelng() {
		return nelng;
	}

	/**
	 * @param nelng
	 *            the nelng to set
	 */
	public void setNelng(double nelng) {
		this.nelng = nelng;
	}

	/**
	 * @return the selat
	 */
	public double getSelat() {
		return selat;
	}

	/**
	 * @param selat
	 *            the selat to set
	 */
	public void setSelat(double selat) {
		this.selat = selat;
	}

	/**
	 * @return the selng
	 */
	public double getSelng() {
		return selng;
	}

	/**
	 * @param selng
	 *            the selng to set
	 */
	public void setSelng(double selng) {
		this.selng = selng;
	}

	/**
	 * @return the schoolname
	 */
	public String getSchoolname() {
		return schoolname;
	}

	/**
	 * @param schoolname
	 *            the schoolname to set
	 */
	public void setSchoolname(String schoolname) {
		this.schoolname = schoolname;
	}

	/**
	 * @return the schoolid
	 */
	public int getSchoolid() {
		return schoolid;
	}

	/**
	 * @param schoolid
	 *            the schoolid to set
	 */
	public void setSchoolid(int schoolid) {
		this.schoolid = schoolid;
	}

	/**
	 * @return the superceded
	 */
	public Boolean getSuperceded() {
		return superceded;
	}

	/**
	 * @param superceded
	 *            the superceded to set
	 */
	public void setSuperceded(Boolean superceded) {
		this.superceded = superceded;
	}

}
